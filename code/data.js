async function loadData(appInstance) {
    try {
        // üö® RUTA CORREGIDA: Apuntar a la subcarpeta /data/ üö®
        const response = await fetch('./data/cursos.json'); 
        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }
        appInstance.STATE.fullData = await response.json();
        log('data', DEBUG_LEVELS.BASIC, "Datos cargados con √©xito.");
    } catch (e) {
        logError('data', "No se pudo cargar 'cursos.json'", e);
        throw e;
    }
}

